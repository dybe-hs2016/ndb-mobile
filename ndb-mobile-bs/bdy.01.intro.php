	<h1> Über die ndb </h1>

	<p class="text"> Die ndb richtet sich an Musikerinnen und Musiker, 
	die ihre Notensammlung digital verwalten möchten. Als registrierte Benutzerin oder registrierter Benutzer 
	können Sie hier Noten erfassen und in ihrer persönlichen Sammlung verwalten. Haben Sie keinen Account, können Sie die in der dnb erfassten Noten durchsuchen. </p>

	<p class="text"> Die ndb entstand im Rahmen des Kurses Dynamische Benutzeroberflächen des Studienganges Informationswissenschaft der HTW Chur im Herbstsemester 2016. </p> 
